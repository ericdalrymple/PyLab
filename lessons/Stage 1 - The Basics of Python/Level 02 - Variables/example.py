# We can use VARIABLES to save VALUES that your program can use later.
# Here we make a VARIABLE called 'message' and we save the VALUE "Hello World!" into it.
message = "Hello world!"

# The 'print' instruction can print the VALUE stored in a VARIABLE. Let's use it to
# print the VALUE we saved in our 'message' VARIABLE.
print(message)

# We can also change the VALUE saved in a VARIABLE
message = "Goodbye World!"

# Let's see what happens if we try to print the 'message' VARIABLE again...
print(message)

# We're now printing the new VALUE of 'message'!
