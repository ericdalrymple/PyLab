# We can use the print instruction to write many things in a row by
# separating them with a comma.
print("Hello", "World!")

# The 'print' instruction can also print numbers. Numbers and variables
# don't need quotation marks ("").
operation_name = "plus"
print(3, operation_name, 1.5, "is", 3 + 1.5)

